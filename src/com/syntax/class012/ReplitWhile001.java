package com.syntax.class012;

public class ReplitWhile001 {

	public static void main(String[] args) {
		int x = 0;
		while (x <= 8) {
			System.out.println(x);
			x++;
		}
		int y = 15;
		while (y >= 0) {
			System.out.println(y);
			y--;
		}
	
		int z = 0;
		while (z<=15) {
			if (z%2==0) {
				System.out.println(z);
			}
				z++;
			}
		}
	
	}
		
