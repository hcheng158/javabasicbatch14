package com.syntax.class010;

public class WhileLoopDemo2 {

	public static void main(String[] args) {
		// print 1 to 10
		int num=10;
		
		while(num<=25) {
			System.out.print(num+", "); //prints space or commas
			num++;
		}

		//print from 10 to 25
		int b=10;
		while(b>=1) {
		System.out.print(b+", ");
		b--;
		}
	}

}
