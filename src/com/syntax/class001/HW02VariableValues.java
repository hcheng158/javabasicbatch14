package com.syntax.class001;

public class HW02VariableValues {

	public static void main(String[] args) {
		
		byte byte1 = 100;
		short short5d = 22334;
		int int6d = 100000; // most popular
		long longcc = 1238546287; // credit cards, transaction ids
		float floatdec = 2.17F;
		double doubleover = 777.158; //popular

		//storing single character
		
		char charB='B';
		char sharp = '#';
		
		//storing yes or no values
		boolean steamy=true;
		boolean redalert=false;
		
		//updating all datatypes with different values
		byte1 = 99;
		short5d = 3445;
		int6d = 200000; // most popular
		longcc = 238546287; // credit cards, transaction ids
		floatdec = 3.17F;
		doubleover = 877.158; //popular
		charB='C';
		sharp = '%';
		steamy=false;
		redalert=true;
		
		System.out.println(byte1);
		System.out.println(short5d);
		System.out.println(int6d);
		System.out.println(longcc);
		System.out.println(floatdec);
		System.out.println(doubleover);
		System.out.println(charB);
		System.out.println(sharp);
		System.out.println(steamy);
		System.out.println(redalert);
	}

}

